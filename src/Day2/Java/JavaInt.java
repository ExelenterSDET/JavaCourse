package Day2.Java;

public class JavaInt {

    static int defaultIntValue;

    public static void main(String[] args) {

        int minimumValue = Integer.MIN_VALUE;
        int maximumValue = Integer.MAX_VALUE;
        int min = -2147483648;
        int max = 2147483647;

        System.out.println("max = " + max);
        System.out.println("min = " + min);
        System.out.println("maximumValue = " + maximumValue);
        System.out.println("minimumValue = " + minimumValue);
        System.out.println("defaultIntValue = " + defaultIntValue);


    }
}
