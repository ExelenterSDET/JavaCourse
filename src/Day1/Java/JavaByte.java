package Day1.Java;

public class JavaByte {
    public static void main(String[] args) {

        byte min = -128;
        byte max = 127;

        byte number1 = 100;
        byte number2 = 100;
        byte total = (byte) (number1 + number2);
        System.out.println("total = " + total);

        System.out.println("min = " + min);
        System.out.println("max = " + max);
    }
}
