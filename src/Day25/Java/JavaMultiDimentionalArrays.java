package Day25.Java;

public class JavaMultiDimentionalArrays {

    public static void main(String[] args) {

        int[][] dArr = {{1,3,5,7,9},{2,4,6,8,10},{0,5,10,15,20,25}};
        System.out.println(dArr[1][2]);
        System.out.println(dArr[0][4]);
        System.out.println(dArr[2][3]);
        System.out.println(dArr[1][0]);
        System.out.println(dArr[0][3]);

    }


}
