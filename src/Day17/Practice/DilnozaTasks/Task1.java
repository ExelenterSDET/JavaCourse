package Day17.Practice.DilnozaTasks;

public class Task1 {

    public static void main(String[] args) {
        // multiplcation table from 1-10

        for (int i = 1; i <=10; i++) {
            for (int j = 1; j <=10 ; j++) {
                System.out.println(i +" * " + j + " = "+ i*j);
            }
            System.out.println();
        }
    }
}
