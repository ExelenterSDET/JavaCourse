package Day17.Practice.DilnozaTasks;

public class Task3 {
    /*
       print grid of "#" with 10 rows and columns
        */
    public static void main(String[] args) {


        for (int i =0; i <=10 ; i++) {
            for (int j = 0; j <=10 ; j++) {
                System.out.print(" # ");

            }
            System.out.println();
        }

    }
}
