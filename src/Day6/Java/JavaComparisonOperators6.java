package Day6.Java;

public class JavaComparisonOperators6 {
    public static void main(String[] args) {
        int number1 = 50;
        int number2 = 50;
        boolean isSmallerOrEqual = number1 <= number2;
        System.out.println("isSmallerOrEqual = " + isSmallerOrEqual);

        number1 = 52;
        number2 = 50;
        isSmallerOrEqual = number1 <= number2;
        System.out.println("isSmallerOrEqual = " + isSmallerOrEqual);

        number1 = 53;
        number2 = 55;
        isSmallerOrEqual = number1 <= number2;
        System.out.println("isSmallerOrEqual = " + isSmallerOrEqual);

    }
}
