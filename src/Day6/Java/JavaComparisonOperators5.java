package Day6.Java;

public class JavaComparisonOperators5 {
    public static void main(String[] args) {

        int number1 = 50;
        int number2 = 50;
        boolean isBiggerOrEqual = number1 >= number2;
        System.out.println("isBiggerOrEqual = " + isBiggerOrEqual);

        number1 = 52;
        number2 = 50;
        isBiggerOrEqual = number1 >= number2;
        System.out.println("isBiggerOrEqual = " + isBiggerOrEqual);

        number1 = 53;
        number2 = 55;
        isBiggerOrEqual = number1 >= number2;
        System.out.println("isBiggerOrEqual = " + isBiggerOrEqual);

    }
}
